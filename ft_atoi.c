#include "libft.h"

int	ft_atoi(const char *s)
{
	long	res;
	int		sign;

	while ((*s >= '\t' && *s <= '\r') || *s == ' ')
		s++;
	sign = (*s != '-') - (*s == '-');
	s += (*s == '-' || *s == '+');
	res = 0;
	while (*s >= '0' && *s <= '9')
	{
		if (res > (LONG_MAX - (*s - '0')) / 10)
			return (-sign * (sign == 1));
		res = (res * 10) + (*s - '0');
		s++;
	}
	return ((int)res * sign);
}
